public class Prepost {
    public static void main(String[] args) {
        int a = 5;

        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        System.out.println("---------------------");

        a = 5;
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);
        System.out.println("---------------------");
        a = 5;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println("---------------------");

        a = 5;
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println("---------------------");        
    }
}
